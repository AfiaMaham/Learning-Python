# Sum of Elements
# Calculate the sum of all the elements in an array.
# Example: Input: [1, 2, 3] → Output: 6

list = [1,2,3]
sum = 0
for i in list:
    sum += i
print(sum)

# Reverse Array
# Write a program to reverse an array.
# Example: Input: [1, 2, 3] → Output: [3, 2, 1]

list.reverse()
print(list)

